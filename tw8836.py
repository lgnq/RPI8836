def write_page(page):
    bus.write_byte_data(ADDR, 0xFF, page)

def write(idx, val):
    bus.write_byte_data(ADDR, idx, val)

def read(idx):
    return bus.read_byte_data(ADDR, idx)
	
def read_id():
	write_page(0x00)
	
	id = read(0x00)
	rev = read(0x01)
	
	return id, rev

def mcu_halt():
	write_page(0x04);

	write(0xED, 0x55)
	write(0xED, 0xAA)
	write(0xEC, 0x00)

def mcu_return():
	write_page(0x04);

	write(0xED, 0x55)
	write(0xED, 0xAA)
	write(0xEC, 0x01)	 

